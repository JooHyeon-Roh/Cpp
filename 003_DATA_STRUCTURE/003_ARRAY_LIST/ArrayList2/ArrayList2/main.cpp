#include <cstdio>
#include "ArrayList.h"

int main(void)
{
	/*** ArrayList의 생성 및 초기화 ***/
	ArrayList list;
	int data;
	Init(&list);

	/*** 5개의 데이터 저장 ***/
	Insert(&list, 11);
	Insert(&list, 11);
	Insert(&list, 22);
	Insert(&list, 22);
	Insert(&list, 33);

	/*** 저장된 데이터의 전체 출력 ***/
	printf("현재 데이터의 수: %d \n", Count(&list));

	if (First(&list, data))    // 첫 번째 데이터 조회
	{
		printf("%d ", data);

		while (Next(&list, data))    // 두 번째 이후의 데이터 조회
			printf("%d ", data);
	}
	printf("\n\n");

	/*** 숫자 22을 탐색하여 모두 삭제 ***/
	if (First(&list, data))
	{
		if (data == 22)
			Remove(&list);

		while (Next(&list, data))
		{
			if (data == 22)
				Remove(&list);
		}
	}

	/*** 삭제 후 저장된 데이터 전체 출력 ***/
	printf("현재 데이터의 수: %d \n", Count(&list));

	if (First(&list, data))
	{
		printf("%d ", data);

		while (Next(&list, data))
			printf("%d ", data);
	}
	printf("\n\n");
	return 0;
}